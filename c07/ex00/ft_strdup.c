#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*str;
	int		len;

	len = 0;
	while (src[len])
		len++;
	str = (char *)malloc(sizeof(*src) * len + 1);
	if (str == NULL)
		return (0);
	len = 0;
	while (src[len])
	{
		str[len] = src[len];
		len++;
	}
	str[len] = '\0';
	return (str);
}
