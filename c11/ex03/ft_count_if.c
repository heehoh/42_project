int	ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	i;
	int	num;

	i = 0;
	num = 0;
	while (i < length)
	{
		if ((*f)(tab[i]) != 0)
			++num;
		++i;
	}
	return (num);
}
